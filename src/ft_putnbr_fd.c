#include "libft.h"

void	ft_putnbr_fd(int nb, int fd)
{
	unsigned int tmp;

	tmp = nb;
	if (nb < 0)
	{
		ft_putchar_fd('-', fd);
		tmp = -nb;
	}
	if (tmp >= 10)
	{
		ft_putnbr_fd(tmp / 10, fd);
		ft_putnbr_fd(tmp % 10, fd);
	}
	else
		ft_putchar_fd(tmp + 48, fd);
}
