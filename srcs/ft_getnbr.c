int		ft_getnbr(char *str)
{
	int nbr;

	nbr = 0;
	if (*str == '-')
		return (-ft_getnbr(str + 1));
	while (*str != '\0')
	{
		nbr = nbr * 10 + (*str - '0');
		str++;
	}
	return (nbr);
}
